/* scheduler.h - Scheduler Interface (XINU Style) */
#ifndef SCHEDULER_H
#define SCHEDULER_H

/*
 * Note: In XINU style, scheduler functions are integrated
 * into the process manager (process.c).
 * 
 * Main scheduling functions:
 *   - resched()  : Reschedule processes
 *   - yield()    : Voluntarily give up CPU
 *   - sleep()    : Sleep for specified ticks
 *   - wait()     : Wait for event
 *   - wakeup()   : Wake processes waiting for event
 * 
 * See process.h for function declarations.
 */

#endif
